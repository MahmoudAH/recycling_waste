        <h2>What is it used for?</h2>
        <p>Cartons are a type of packaging for food and beverage products such as milk, juice, soy milk, soups and broths.
        They are easy to recognize and are available in two types:
<ul>
<li>Shelf-stable cartons preserve the quality of the product for long periods of time without refrigeration or preservatives and are found on grocery store shelves.</li>
<li>Refrigerated gable top cartons are found in the refrigerated section of grocery stores.</li>
</ul></p></br>
        <h2>Why it wants to be recycled.</h2>
        <p>
            Cartons contain some of the highest-quality virgin fiber still available in the recycling stream. Materials recovered from recycled cartons save us from depleting our environment’s precious resources and can become new products like napkins, tissue paper, paper towels and even building materials.
            Not to mention that every time you recycle your cartons, you’re taking one important step to reduce landfill waste.
</p></br>
  <h2>How to recycle it?</h2>
        <p>
            Recycling your cartons is easy
            <ol>
           <li> Empty it out: Empty out that unused juice or broth from your carton before recycling.
</li>

    <li>Toss the carton – without flattening – into your recycling bin along with all your other recyclable containers (pro tip:
you can keep the cap on and straws!).</li>
    
<li>Take ‘em to the curb: On pick-up day, roll your recycling bin out to the curb to be collected.</li>
        </ol>
        </p></br>
        <h2>What does recycled paper become?</h2>
        <p>Recycled cartons are turned into products you use every day, like tissues, paper towels, toilet paper, writing and office paper.
        They also become environmentally friendly building materials, like ceiling and roofing tiles.

</p>
  
