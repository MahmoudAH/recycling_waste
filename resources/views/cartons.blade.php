        <h2>Why it wants to be recycled.</h2>
        <p>
            Cartons contain some of the highest-quality virgin fiber still available in the recycling stream. Materials recovered from recycled cartons save us from depleting our environment’s precious resources and can become new products like napkins, tissue paper, paper towels and even building materials.
